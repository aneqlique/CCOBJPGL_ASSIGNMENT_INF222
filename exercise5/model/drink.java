package model;

class drink {

    private String feels;
    private String taste;

    public String getFeels() {
        return this.feels;
    }

    public void setFeels(String feels) {
        this.feels = feels;
    }

    public String getTaste() {
        return this.taste;
    }

    public void setTaste(String taste) {
        this.taste = taste;
    }
}